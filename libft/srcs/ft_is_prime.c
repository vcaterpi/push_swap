#include "libft.h"

int		ft_is_prime(int nb)
{
	int d;

	d = 2;
	if (nb <= 1)
		return (0);
	while (d * 2 <= nb)
	{
		if (nb % d == 0)
			return (0);
		d++;
	}
	return (1);
}
